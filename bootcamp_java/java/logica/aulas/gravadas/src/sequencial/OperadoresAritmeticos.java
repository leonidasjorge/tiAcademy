package sequencial;

public class OperadoresAritmeticos {

	public static void main(String[] args) {
		
		System.out.println(((5 * 6) / 3)); // Resultado = 10;
		System.out.println((5 + (2 * 4))); // Resultado = 13;
		System.out.println(((5 + 2) * 4)); // Resultado = 28;
		System.out.println(((70 / (5 + 2)) * 4)); // Resultado = 40;
		System.out.println(70 / ((5 + 2) * 4)); // Resultado = 2;
		System.out.println(18 / 4); // Resultado = 4;
		System.out.println(18.0 / 4.0); // Resultado = 4.5;
		System.out.println(18 % 4); // Resultado = 2;

	}

}
